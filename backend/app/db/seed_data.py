def _sync_formula_herb_relations(
    db: Session, formula_map: dict[str, Formula], herb_map: dict[str, Herb]
) -> None:
    formula_ids = [formula.id for formula in formula_map.values()]
    herb_ids = [herb.id for herb in herb_map.values()]
    existing_relations = {
        (relation.formula_id, relation.herb_id): relation
        for relation in db.query(FormulaHerbRel)
        .filter(FormulaHerbRel.formula_id.in_(formula_ids))
        .filter(FormulaHerbRel.herb_id.in_(herb_ids))
        .all()
    }

    for item in FORMULA_HERB_RELATION_DATA:
        formula = formula_map[item["formula_name"]]
        herb = herb_map[item["herb_name"]]
        relation_key = (formula.id, herb.id)
        relation = existing_relations.get(relation_key)

        if relation:
            continue

        db.add(
            FormulaHerbRel(
                formula_id=formula.id,
                herb_id=herb.id,
                amount_desc=item["amount_desc"],
            )
        )


def seed_sample_data(db: Session) -> None:
    category_map = _get_or_create_categories(db)
    herb_map = _get_or_create_herbs(db, category_map)
    formula_map = _get_or_create_formulas(db, category_map)
    _sync_formula_herb_relations(db, formula_map, herb_map)
    db.commit()
