def _category(category_type: str, name: str, description: str) -> dict:
    return {
        "category_type": category_type,
        "name": name,
        "description": description,
    }


def _herb(
    name: str,
    alias: str,
    category_name: str,
    nature_flavor: str,
    meridian_tropism: str,
    efficacy: str,
    indication: str,
    usage_method: str,
    precaution: str,
    source_text: str,
) -> dict:
    return {
        "name": name,
        "alias": alias,
        "category_name": category_name,
        "nature_flavor": nature_flavor,
        "meridian_tropism": meridian_tropism,
        "efficacy": efficacy,
        "indication": indication,
        "usage_method": usage_method,
        "precaution": precaution,
        "source_text": source_text,
    }


def _formula(
    name: str,
    category_name: str,
    composition: str,
    efficacy: str,
    indication: str,
    usage_method: str,
    source_text: str,
) -> dict:
    return {
        "name": name,
        "category_name": category_name,
        "composition": composition,
        "efficacy": efficacy,
        "indication": indication,
        "usage_method": usage_method,
        "source_text": source_text,
    }


def _relation(formula_name: str, herb_name: str, amount_desc: str) -> dict:
    return {
        "formula_name": formula_name,
        "herb_name": herb_name,
        "amount_desc": amount_desc,
    }


